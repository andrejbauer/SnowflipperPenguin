package logika;

public class Vektor {
	
	// Razred uporabljamo za smeri s pomočjo katerih pregledujemo ploščo
	
	private int x;
	private int y;
	
	public Vektor(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
}
